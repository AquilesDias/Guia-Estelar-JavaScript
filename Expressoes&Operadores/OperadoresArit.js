/*
   Operadores Aritiméticos
*/

//Multiplação *
console.log(2 * 2.2)//Na tela: 4.4

//Divisão /
console.log(12/2);//Na tela: 6

//Soma  +
console.log(12+12)//Na tela: 24

//Subtração  -
console.log(12-6)//Na tela: 6

//Resto da divisão  %
console.log(12%2)// Na tela: 0

//Incremento  ++
let increment = 2;
increment++;
console.log(increment)//Na tela: 3

//Descremento  --
let decremento = 2;
decremento--;
console.log(decremento)//Na tela: 1

//Exponencial **
console.log(2**2)//Na tela: 4

